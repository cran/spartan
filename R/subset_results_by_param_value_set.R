subset_results_by_param_value_set<-function(PARAMETERS,RESULT_SET,PARAMETER_VALUES_TO_FILTER_BY)
{
	# TAKES A CSV FILE OF MEDIAN RESULTS OR SIMULATION RESULTS AND FILTERS BY A SPECIFIED SET OF PARAMETER VALUES
	# RETURNING THE FILTERED SET

	# TAKE A COPY OF THE RESULT SET
	PARAM_RESULT<-RESULT_SET
	#print(paste("PARAMETERS ",length(PARAMETERS),sep=""))

	# NOW EXTRACT THE DATA FOR THIS PARAM VALUE
	for(PARAMOFINT in 1:length(PARAMETERS))
	{
		PARAM_RESULT<-subset(PARAM_RESULT,PARAM_RESULT[[PARAMETERS[PARAMOFINT]]]==as.numeric(PARAMETER_VALUES_TO_FILTER_BY[PARAMOFINT]))
	}

	return(PARAM_RESULT)
}

